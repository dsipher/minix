#include <sys/inode.h>
#include <sys/dev.h>
#include <sys/spin.h>
#include "config.h"

/* allocated and zeroed at boot by pginit() in page.c */

struct inode *inode;            /* inode[NINODE] */
struct inodeq *inodeq;          /* inodeq[NINODEQ] */

/* an inode with no active references (i_refs == 0) go
   here, and can be assigned to a different dev/ino */

static struct inodeq iavailq = TAILQ_HEAD_INITIALIZER(iavailq);

/* protects inode queues, and the
   i_busy/i_wanted fields in inode */

static spinlock_t inode_lock;

/* map an inode number to its bucket.
   efficient if NINOQ is a power of 2 */

#define INOHASH(ino)    ((ino) % NINODEQ)

/* initialize inoq buckets, then initialize all inodes to an impossible
   device, scatter them across the buckets, then put them on iavailq. */

void
inoinit(void)
{
    int i, b;

    for (i = 0; i < NINODEQ; ++i)
        TAILQ_INIT(&inodeq[i]);

    for (i = 0; i < NINODE; ++i)
    {
        inode[i].i_dev = NODEV;
        inode[i].i_ino = i;
        b = INOHASH(inode[i].i_ino);
        TAILQ_INSERT_TAIL(&inodeq[b], &inode[i], i_hash_links);
        TAILQ_INSERT_TAIL(&iavailq, &inode[i], i_avail_links);
    }
}

